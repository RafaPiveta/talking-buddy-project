  const originalText = new WeakMap();
  const originalAttributes = new WeakMap();
  const translatedAttributes = ["alt", "aria-label", "placeholder", "title"];
  let currentLanguage = supportedLanguages.includes(localStorage.getItem(storageKey))
    ? localStorage.getItem(storageKey)
    : "pt";

  function compact(text) {
    return String(text || "").replace(/\s+/g, " ").trim();
  }

  function translateText(text, lang = currentLanguage) {
    const original = compact(text);
    if (!original || lang === "pt") return original;
    return dictionaries[lang]?.[original] || original;
  }

  function shouldSkipTextNode(node) {
    const parent = node.parentElement;
    if (!parent) return true;
    return ["SCRIPT", "STYLE", "NOSCRIPT", "SVG", "CANVAS"].includes(parent.tagName);
  }

  function applyTextNode(node) {
    if (shouldSkipTextNode(node)) return;
    if (!originalText.has(node)) originalText.set(node, node.nodeValue);

    const original = originalText.get(node);
    const key = compact(original);
    if (!key) {
      node.nodeValue = original;
      return;
    }

    const leading = original.match(/^\s*/)?.[0] || "";
    const trailing = original.match(/\s*$/)?.[0] || "";
    node.nodeValue = `${leading}${translateText(original)}${trailing}`;
  }

  function applyElementAttributes(element) {
    translatedAttributes.forEach((attribute) => {
      if (!element.hasAttribute(attribute)) return;

      let stored = originalAttributes.get(element);
      if (!stored) {
        stored = {};
        originalAttributes.set(element, stored);
      }

      if (!(attribute in stored)) stored[attribute] = element.getAttribute(attribute);
      const original = stored[attribute] || "";
      const translated = translateText(original);
      element.setAttribute(attribute, translated);
    });
  }

  function applyTranslations(root = document.body) {
    if (!root) return;

    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
    while (walker.nextNode()) applyTextNode(walker.currentNode);

    root.querySelectorAll?.("*").forEach(applyElementAttributes);

    document.documentElement.lang = currentLanguage === "pt"
      ? "pt-BR"
      : currentLanguage === "es"
        ? "es"
        : "en";
  }

  function updateControls() {
    document.querySelector(".lang-switch")?.setAttribute("data-current", currentLanguage);
    document.querySelectorAll("[data-lang]").forEach((button) => {
      const active = button.dataset.lang === currentLanguage;
      button.classList.toggle("is-active", active);
      button.setAttribute("aria-pressed", active ? "true" : "false");
    });
  }

  function setLanguage(lang) {
    if (!supportedLanguages.includes(lang)) return;
    currentLanguage = lang;
    localStorage.setItem(storageKey, lang);
    applyTranslations();
    updateControls();
    window.dispatchEvent(new CustomEvent("talkingbuddy:languagechange", { detail: { language: lang } }));
  }

  function init() {
    document.querySelectorAll("[data-lang]").forEach((button) => {
      button.addEventListener("click", () => setLanguage(button.dataset.lang));
    });
    setLanguage(currentLanguage);
  }

  window.TalkingBuddyI18n = {
    apply: applyTranslations,
    getLanguage: () => currentLanguage,
    setLanguage,
    translate: translateText,
  };

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init, { once: true });
  } else {
    init();
  }
